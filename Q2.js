function addnum(number1, number2) {
    var result = number1 + number2;
    return result;

}

function multiNum(number1,number2){
    var result = number1 * number2;
    return result
}

function substractNum(number1,number2){
    var result = number1 - number2;
    return result
}

function divideNum(number1,number2){
    var result = number1 / number2;
    return result
}

